package com.box.entity;

public class Items {
	private int itemId;
	private String itemName;
	private int typeId;
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	@Override
	public String toString() {
		return "Items [itemId=" + itemId + ", itemName=" + itemName + ", typeId=" + typeId + "]";
	}
	public Items(int itemId, String itemName, int typeId) {
		super();
		this.itemId = itemId;
		this.itemName = itemName;
		this.typeId = typeId;
	}
	public Items() {
		super();
	}
	

}
